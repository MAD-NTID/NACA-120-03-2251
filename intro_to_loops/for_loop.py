# Loop from 0 to 9
print("range(10)")
for i in range(10):
    print(i)

print()

print("range(1, 11)")
# Loop from 1 to 10
for i in range(1, 11):
    print(i)

print()

# Print odd numbers
print("range(1, 11, 2)")
for i in range(1, 11, 2):
    print(i)

print()

print("range(-10, 1, 2)")
for i in range(-10, 1, 2):
    print(i)

print()

# even numbers
print("range(2, 51, 2)")
for i in range(2, 51, 2):
    print(i)